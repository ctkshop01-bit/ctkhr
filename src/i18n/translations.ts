export function translate(lang: Lang, key: string, params?: Record<string, string | number>) {
  const dict = lang === "th" ? th : zh;
  const raw = dict[key] ?? zh[key] ?? key;
  if (!params) return raw;
  return Object.entries(params).reduce((acc, [k, v]) => acc.replace(new RegExp(`\\{${k}\\}`, "g"), String(v)), raw);
}
